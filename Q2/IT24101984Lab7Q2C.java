public class IT24101984Lab7Q2C {
    public static void main(String[] args) {

        int i,j;
        int k =5;

        //Outer loop
        for(i = 5; i>=1; i--) {

            //Inner Loop
            for(j = 1; j <= k; j++) {
                System.out.print(i);
                
            }
            k--;
            System.out.print("\n");
        
        }



    }
}
 